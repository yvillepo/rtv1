#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t n)
{
	char	*d;

	d = dst;
	while (*src && n > 0)
	{
		--n;
		*d++ = *src++;
	}
	while (n-- > 0)
		*d++ = 0;
	return (dst);
}
